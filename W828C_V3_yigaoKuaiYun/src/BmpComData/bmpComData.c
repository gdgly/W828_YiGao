/*---------------------------------------------------------------------------
* 函数: bmpcom_get_attr_data
* 说明: 获取公共bmp图片的属性，不是公共的图片不要加入其中的数组中
* 参数: 
* 返回:	
*-----------------------------------------------------------------------------*/
void bmpcom_get_attr_data(tBMP_ATTR **ppBmpAttr, eBMP_DATA_ID  id)
{
	  if (id >= BMP_ID_MAX) 
	  {
	      *ppBmpAttr = NULL;
	      return ; 
	  }    
	  
	  *ppBmpAttr = &gtComBmpAttr[id]; 
}